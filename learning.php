		<h4>LANGUAGE AS A TOOL FOR COMMUNICATION </h4>
According to Awoniyi (1982:2), communication is the means by which we pass on from one person to another our ideas, our feelings, our knowledge, our requests, and indeed every aspect of human life.  Man’s use of language is therefore socially determined to express self, thoughts, organize his world and transmit his feelings, ideas and information.  According to Fromkin and Rodman (1978), the need for language became quite strong with communal pattern of living.  Language plays the social roles of establishing and strengthening social ties and influencing others.  Hymes (1964) adds that the origin of language resides largely on the necessity of man to transfer his inner thoughts, ideas, and cumulative experience to another for the purpose of behaviour control or to impose order in his community.  He explains that the early man soon began to live with his neighbours and the language he acquired and used was a combination of cries and gestures or unstructured language which with time and necessity acquired shape and structure. 
A review of these few definitions of language reveals that language is mainly for the purpose of communication. Wardhaugh (1986:3) defines language as a “system of arbitrary vocal symbols used for human communications.”  Sapir (1921:6) sees language as “a purely human and non-instinctive method of communicating ideas, emotions and desires by means of a system of voluntarily produced symbols.”  Trudgill (1966:4) shares the same view when he defines language as “a means of communicating information as well as establishing and maintaining relationships with other people.” 
The common feature in the above definitions of language is that language is used for communication and that man has always used language to facilitate social communication.
