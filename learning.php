<center><h2>ETUNO ORTHOGRAPHY </h2></center>
The study of the adaptation of loan words from the English language into the Etuno language will be better understood with the knowledge of its orthography i.e. its alphabet and system of spellings. 
The alphabet of the Etuno language as contained in the Etuno Primer (1992:5) and in the work of Amune (1991:7) have little variations concerning the existence and nonexistence of certain letters.  These were examined in the work of Adeleye (2001:20) in which he presented what can be considered to be the alphabet of the Etuno language.  
<br>They are reproduced below: <hr>
	<center>a   b   ch   d   e   e   f   g   j   k   m   n   nw  ny   o   o   p   r   s   s   t   u   v   w   y   z 
</center>
The above presentation is slightly different from what is obtainable in both the Etuno Primer and Amune’s work.  Where the Etuno Primer has “ch”, Amune has “c”. Adeleye’s work favoured the ‘ch’ because there is neither a sound nor a word that tallies with the letter ‘c’ in the Etuno language. 
Despite the absence of the English letters ‘h’, ‘I’, ‘q’ and ‘x’ from the Etuno alphabet, the Etuno language still has 26 letters owing to the presence of non-independent letters and those which have a diacritical symbol of dot underneath. 

