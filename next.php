	 The following represents the vowel sounds of the Etuno language: <br>
			1.	/l/  	-  	usi (thigh), ozi (child). <br><br>
			2.	/e/ 	- 	age (kettle, igene (headgear). <br><br>
			3.	/e/ 	- 	ireta (stone), ufe (moon). <br><br>
			4.	/a/ 	- 	aza (people), asise (feather). <br><br>
			5.	/o/ 	- 	oromi (orange), unomi (bird). <br><br>
			6.	/  :/ 	- 	onoru (man or male), onyenne (woman or female). <br><br>
			7.	/ u / 	- 	uvo (hand), uzomi (star). <br><br>
			8.	/ei/ 	- 	eyi (face), oreyi (mirror). <br><br>
			9.	/  l/ 	- 	oonoi (umbrella), oiku (anger). <br><br>

